class JT_Printer:
    mg_print_line = []
    mg_exec_print_line = []
    
#==================================================================
#
#==================================================================

    def get_print_line(self):
        self.init_print_line()
        return self.mg_print_line[0]

#==================================================================
#
#==================================================================

    def get_exec_print_line(self):
        self.init_print_line()
        return self.mg_exec_print_line[0]

#==================================================================
#
#==================================================================
    
    def set_to_print_line(self):
        self.init_print_line()
        self.mg_print_line[0] = 1
        self.mg_exec_print_line[0] = 1

#==================================================================
#
#==================================================================

    def set_exec_to_print_line(self):
        self.init_print_line()        
        self.mg_exec_print_line[0] = 1
#==================================================================
#
#==================================================================
        
    def uset_exec_to_print_line(self):
        self.init_print_line()        
        self.mg_exec_print_line[0] = 0
                    
#==================================================================
#
#==================================================================

    def uset_to_print_line(self):
        self.init_print_line()
        self.mg_print_line[0] = 0
        self.mg_exec_print_line[0] = 0

#==================================================================
#
#==================================================================

    def init_print_line(self):
        if(len(self.mg_print_line) == 0):
            self.mg_print_line.append(0)
           
        if(len(self.mg_exec_print_line) == 0):           
            self.mg_exec_print_line.append(0)
            
#==================================================================
#
#==================================================================
        
    @staticmethod        
    def DEF_PRINT_LONG_LINE_TXT():
        return 0
#==================================================================
#
#==================================================================
        
    @staticmethod                
    def DEF_PRINT_SHORT_LINE_TXT():
        return 1

          
