import logging

#==================================================================
#
#                    JT_StaticLogger
#
#==================================================================

class JT_LoggerHelperGlobal:
    _shared_state_ = {}
    m_log = logging.getLogger("finder_0")            
    
#==================================================================
#
#==================================================================
            
    def __init__(self):
        self.__dict__ = self.__shared_state        
        
#==================================================================
#
#==================================================================
        
    def log_init(self):
        self.m_log.setLevel(logging.DEBUG)
        self.m_log.setLevel(logging.INFO)
