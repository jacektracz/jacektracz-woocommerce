class JT_ArgParams:
#==================================================================
#
#==================================================================

    def __init__(self):
        self.m_Params = []

#==================================================================
#
#==================================================================

    def add_param(self,dd):
        self.m_Params.append(dd)
        
#==================================================================
#
#==================================================================
        
    def add_param_s(self,sname):
        dd = JT_ArgParam()
        dd.m_ParamName = sname
        self.m_Params.append(dd)

