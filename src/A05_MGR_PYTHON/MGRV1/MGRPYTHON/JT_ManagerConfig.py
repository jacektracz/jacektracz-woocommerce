class JT_ManagerConfig:
    mg_filtr_ps = []
    @staticmethod        
    def DEF_USE_SU_FOR_ANOTHER_USERS():
        return 1
        
#==================================================================
#
#==================================================================

#==================================================================
#
#==================================================================
        
    def init_manager_config(self):
        if(len(self.mg_filtr_ps) == 0):
            self.mg_filtr_ps.append(0)

#==================================================================
#
#==================================================================

    def get_filtr_ps(self):
        self.init_manager_config()
        return self.mg_filtr_ps[0]
                    
