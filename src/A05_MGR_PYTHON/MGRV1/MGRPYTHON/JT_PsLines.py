from JT_PsLine import JT_PsLine
#==================================================================
#
#==================================================================
                
class JT_PsLines:
    def __init__(self):
        self.m_ps_lines = []
    
#==================================================================
#
#==================================================================
    
    def add_ps_line(self,pline):
        dd = JT_PsLine()        
        dd.m_Line = pline
        self.m_ps_lines.append(dd)
        
        
#==================================================================
#
#==================================================================

    def internal_init(self):
        self.m_ps_lines=[]
        
#==================================================================
#
#==================================================================
                
    def get_ps_line(self,name):
        dd = JT_PsLine()        
        return dd
        
