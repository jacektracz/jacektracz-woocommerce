class JT_LoggerSettings:
    
    mg_to_stdout = []
    def __init__(self):        
        self.mg_to_stdout.append(0)

#==================================================================
#    
#==================================================================
        
    def STDOUT_HARD(self):
        self.init_output()
        return self.mg_to_stdout[0]

#==================================================================
#    
#==================================================================
    
    def set_to_output(self):
        self.init_output()
        self.mg_to_stdout[0] = 1

#==================================================================
#    
#==================================================================
    def init_output(self):
        if(len(self.mg_to_stdout) == 0 ):
            self.mg_to_stdout.append( 0 )
        
        
#==================================================================
#    
#==================================================================
              
    def uset_to_output(self):
        self.init_output()
        self.mg_to_stdout[0] = 0
