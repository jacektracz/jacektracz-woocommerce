class JT_ProcessesDef :
    
#==================================================================
#
#==================================================================
    
    def __init__ (self):
        self.m_processess = []    
        
#==================================================================
#
#==================================================================
        
    def add_process_to_processes(self,pp):
        self.m_processess.append(pp)

#==================================================================
#
#==================================================================
        
    def print_processess(self):
        for dd_pp in self.m_processess:
            dd_pp.print_process()

